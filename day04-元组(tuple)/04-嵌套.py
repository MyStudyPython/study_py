"""
嵌套

由于元组和列表都可以充当`容器`，
他们内部可以放很多元素，
并且也支持元素内的各种嵌套。
"""
tu = (
    "今天姐姐不在家",
    "姐夫和小姨子在客厅聊天",
    ("姐夫问小姨子税后多少钱", "小姨子低声说道说和姐夫还提钱"),
    ["范德彪", "刘华强", "尼古拉斯赵四", "宋小宝", "刘能", [1, 2, 3, 4]],
)

tu1 = tu[0]  # 今天姐姐不在家
tu2 = tu[1]  # 姐夫和小姨子在客厅聊天
tu3 = tu[2][0]  # 姐夫问小姨子税后多少钱
tu4 = tu[2][1]  # 小姨子低声说道说和姐夫还提钱
tu5 = tu[2][1][3]  # 低

"""
元组的元素不可以被替换，元素个数也不允许被修改，只能查看（取值）
"""
# tu[0] = 123  # 报错
# tu[-1] = 666  # 报错

tu[-1][0] = "天五天"  # 正确
